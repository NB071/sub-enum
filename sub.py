def run_tool(command, output_file, tool_name):
    try:
        result = subprocess.run(command, capture_output=True, text=True)
        if result.returncode == 0:
            print(f"[‚úì] {tool_name} completed successfully.")
            # Save the result to the output file
            with open(output_file, 'w') as file:
                file.write(result.stdout)
        else:
            print(f"[‚úó] {tool_name} error: {result.stderr}")
    except Exception as e:
        print(f"[‚úó] Error running {tool_name}: {e}")

def append_to_file(output_file, content_file):
    try:
        with open(content_file, 'r') as file:
            content = file.readlines()
        with open(output_file, 'a') as file:
            file.writelines(content)
    except Exception as e:
        print(f"[‚úó] Error appending to file: {e}")

def fetch_crtsh_subdomains(domain):
    url = f"https://crt.sh/?q={domain}&output=json"
    try:
        response = requests.get(url)
        response.raise_for_status()
        data = response.json()
        subdomains = set()
        for entry in data:
            # Extract subdomains from the JSON response
            subdomain = entry.get('name_value', '')
            if domain in subdomain:
                subdomains.add(subdomain)
        return list(subdomains)
    except Exception as e:
        print(f"[‚úó] Error fetching data from crt.sh: {e}")
        return []

def deduplicate_file(file_path):
    try:
        with open(file_path, 'r') as file:
            lines = file.readlines()
        
        # Remove duplicates by converting list to a set
        unique_lines = set(line.strip() for line in lines)
        
        with open(file_path, 'w') as file:
            file.write("\n".join(unique_lines) + "\n")
            
        print(f"[‚úì] Deduplication complete. Unique entries saved to '{file_path}'.")
    except Exception as e:
        print(f"[‚úó] Error during deduplication: {e}")

